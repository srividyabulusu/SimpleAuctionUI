package application;

public class SearchCustResultBean {
	private String name = null;
	private String rating_buyer_to_seller = null;
	private String avg_rating = null;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRating_buyer_to_seller() {
		return rating_buyer_to_seller;
	}
	public void setRating_buyer_to_seller(String rating_buyer_to_seller) {
		this.rating_buyer_to_seller = rating_buyer_to_seller;
	}
	public String getAvg_rating() {
		return avg_rating;
	}
	public void setAvg_rating(String avg_rating) {
		this.avg_rating = avg_rating;
	}
}
